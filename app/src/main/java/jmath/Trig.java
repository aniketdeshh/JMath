package jmath;

public class Trig {

    public static final double PI = Math.PI;
    public static final double DEG_TO_RAD = PI / 180;
    public static final double RAD_TO_DEG = 180 / PI;

    public static double sin(double rad) {
        return Math.sin(rad);
    }

    public static double asin(double rad) {
        return Math.asin(rad);
    }

    public static double cos(double rad) {
        return Math.cos(rad);
    }

    public static double acos(double rad) {
        return Math.acos(rad);
    }

    public static double tan(double rad) {
        return Math.tan(rad);
    }

    public static double atan(double rad) {
        return Math.atan(rad);
    }

}
